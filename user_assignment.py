def assign_users_to_project(project_id, users_roles):
    # Logic to assign users to a project based on roles and responsibilities
    assigned_users = []
    for user, role in users_roles.items():
        # Placeholder logic for role-based assignment
        print(f"Assigning {user} with role {role} to project {project_id}...")
        assigned_users.append(user)
    return assigned_users